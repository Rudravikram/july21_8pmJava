package com.maths;

public class Math_Methods {

	public static void main(String[] args) {
		System.out.println(Math.max(2, 10));
		System.out.println(Math.min(2, 10));
		System.out.println(Math.pow(5, 10));
		System.out.println(Math.sqrt(52));
		System.out.println(Math.abs(-15.23));
		System.out.println(Math.random());
		System.out.println(Math.round(52.13));
		System.out.println(Math.ceil(15.69));
		System.out.println(Math.floor(15.96));

	}

}


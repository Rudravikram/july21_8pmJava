package com.variable;

public class Static_variable {

	
	 static int num = 180;
	 static char c = 'a';
	 static boolean b = true;
	 static byte n = 98;
	 static short p = 150;
	 static long x = 12345578;
	 static float w = 1234.78f;
	 static double m = 32.45d;
	 
	 
	public static void main(String[] args) {
          
		System.out.println(Static_variable.num);
		System.out.println(Static_variable.c);
		System.out.println(Static_variable.b);
		System.out.println(Static_variable.n);
		System.out.println(Static_variable.p);
		System.out.println(Static_variable.x);
		System.out.println(Static_variable.w);
		System.out.println(Static_variable.m);
		
		
	}

}



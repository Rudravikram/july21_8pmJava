package com.variable;

public class Instance_Variable {

	int num = 500;
	char c ='c';
	byte b = 100;
	short m = 1000;
	boolean n = true;
	long a = 5677777;
	float r =  54.2f;
	double d = 444444.6d;
	
	public static void main(String[] args) {
		Instance_Variable obj = new Instance_Variable();
		System.out.println(obj.num);
		
		Instance_Variable jnm = new Instance_Variable();
		System.out.println(jnm.c);
		
		Instance_Variable kju = new Instance_Variable();
		System.out.println(kju.b);
		
		Instance_Variable fdf = new Instance_Variable();
		System.out.println(fdf.m);
		
		Instance_Variable cvc = new Instance_Variable();
		System.out.println(cvc.n);
		
		Instance_Variable rew = new Instance_Variable();
		System.out.println(rew.a);
		
		Instance_Variable gft = new Instance_Variable();
		System.out.println(gft.r);
		
		Instance_Variable trm = new Instance_Variable();
		System.out.println(trm.d); 
		
		
    }

}


